package step04;

public class Exam40_0 {
  public static void main(String[] args) {
    //논리연산자의 결과는 논리 값이다.
    //피연산자는 논리 값이어야 한다.
    //boolean v = 10 && 20;
    boolean v = true && true;
    System.out.println(v);
    System.out.println(true && false);
    System.out.println(false && true);
    System.out.println(false && false);

    System.out.println("------------------------------------");
    
    System.out.println(true || true);
    System.out.println(true || false);
    System.out.println(false || true);
    System.out.println(false || false);


















  }
}
