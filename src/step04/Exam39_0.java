//subject: 연산자 - 관계연산자
package step04;

public class Exam39_0 {
  public static void main(String[] args) {
    boolean v = 10 == 20;         // 관계 연산자의 결과는 논리 값이다.
    System.out.println(v);

    v = 10 != 20;
    System.out.println(v);

    v = 10 < 20;
    System.out.println(v);

    v = 10 <= 20;
    System.out.println(v);

    v = 10 > 20;
    System.out.println(v);

    v = 10 >= 20;
    System.out.println(v);

    System.out.println(3.14f > 3.12f);

    System.out.println(3.14 > 3.14000000000);

    System.out.println('가' > '각');


















  }
}
