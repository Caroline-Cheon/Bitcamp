package step02;

public class Exam23 {
  public static void main(String[] args) {
    System.out.println(100);          //10진수
    System.out.println(0144);         // 8
    System.out.println(0x64);         //16
    System.out.println(0X64);
    System.out.println(0b01100100);          //2진수(주로 0b사용)
    System.out.println(0B01100100);
  }
}
